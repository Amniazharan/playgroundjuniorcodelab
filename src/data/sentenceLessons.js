export const getSentenceLessonById = (id) => {
  return sentenceLessons.find(lesson => lesson.id === parseInt(id));
};

// Helper function untuk dapatkan lessons by difficulty
export const getSentenceLessonsByDifficulty = (difficulty) => {
  return sentenceLessons.filter(lesson => lesson.difficulty === difficulty);
};
